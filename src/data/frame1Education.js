/** Maps highest pill → legacy `edu` used downstream (12 / UG / PG). */
export function legacyEduFromMax(eduMax) {
  if (!eduMax) return '12'
  if (eduMax === '10_below' || eduMax === '12_pass' || eduMax === 'diploma') return '12'
  if (eduMax === 'iti' || eduMax === 'graduate') return 'UG'
  if (eduMax === 'postgraduate') return 'PG'
  return '12'
}

