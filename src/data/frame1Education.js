const ALL_COLLEGES = [...new Set([...TIER1, ...TIER2, ...TIER3])].sort((a, b) => a.localeCompare(b))

export function searchIndianColleges(query, limit = 24) {
  const q = String(query || '')
    .trim()
    .toLowerCase()
  if (!q) return ALL_COLLEGES.slice(0, limit)
  const hit = ALL_COLLEGES.filter((c) => c.toLowerCase().includes(q))
  return hit.slice(0, limit)
}

export function completionYearOptions() {
  const y = new Date().getFullYear()
  const out = []
  for (let i = 0; i <= 55; i++) out.push(String(y - i))
  return out
}
