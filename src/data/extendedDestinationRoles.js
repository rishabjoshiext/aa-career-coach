import { cloneRoleCard } from '../utils/fasttrackData.js'

/**
 * @param {string} industry
 * @param {string} roleTitle
 * @returns {Record<string, any> | null}
 */
export function resolveDestinationCard(industry, roleTitle) {
  const fromCatalog = cloneRoleCard(industry, roleTitle)
  if (fromCatalog) return { ...fromCatalog }
  const row = EXTENDED_BY_KEY.get(`${industry}|||${roleTitle}`)
  return row ? { ...row, industry } : null
}

